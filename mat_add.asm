ASSUME CS:CODE, DS:DATA
DATA SEGMENT
MAT1 DW 0001H, 0002H, 0003H, 0004H, 0005H, 0006H, 00007H, 00008H
MAT2 DW 0009H, 0008H, 0007H, 0006H, 0005H, 0004H, 0003H, 0002H
MAT3 DW 4 DUP(?)
CNT EQU 00H
CNT1 EQU 08H
DATA ENDS
CODE SEGMENT
START:
MOV AX, DATA
MOV DS,AX
MOV CX, CNT-1
MOV AH, 02H
XOR AX,AX
LOOP1:
MOV SI, OFFSET MAT1
ADD AH, 02H
MOV DX,SI
ADD DL,AH
MOV SI,DX
MOV AL,[SI]
MOV SI, OFFSET MAT2
MOV DX,SI
ADD DL,AH
MOV SI,DX
MOV BL,[SI]
ADD BL,AL
MOV SI,OFFSET MAT3
MOV DX,SI
ADD DL,AH
MOV SI,DX
MOV [SI],BL
LOOP LOOP1
HLT
CODE ENDS
END START
